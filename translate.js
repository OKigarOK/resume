let currentLang = ru;
translate();

function translate() {
    const elementsToTranslate = document.querySelectorAll('[my-translate]')

    for (let element of elementsToTranslate) {
        const key = element.getAttribute('my-translate');
        element.textContent = currentLang[key];
    }
}

function changeToRu() {
    currentLang = ru;
    setRuIcon()
    translate();
}

function changeToEn() {
    currentLang = en;
    setEnIcon()
    translate();
}

function setEnIcon() {
    const element = document.querySelector('.set_lang')
    element.firstElementChild.classList.add('icon_opacity')
    element.lastElementChild.classList.remove('icon_opacity')
}

function setRuIcon() {
    const element = document.querySelector('.set_lang')
    element.firstElementChild.classList.remove('icon_opacity')
    element.lastElementChild.classList.add('icon_opacity')
}
